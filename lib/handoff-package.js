const fs = require('fs');
const path = require('path');
const { createScaffold, bootstrapScaffold } = require('./scaffold-generator');

function writeFileIfContent(filePath, content) {
  if (content === null || content === undefined) return false;
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, 'utf8');
  return true;
}

function wrapPrototypeHtml(prototypeHtml) {
  if (!prototypeHtml || !prototypeHtml.trim()) return '';
  return `<!DOCTYPE html>\n<html>\n<head>\n  <meta charset="UTF-8">\n  <meta name="viewport" content="width=device-width, initial-scale=1.0">\n</head>\n<body>\n${prototypeHtml}\n</body>\n</html>`;
}

function prototypeFromBlueprint(blueprint = '') {
  const htmlBlockMatch = String(blueprint || '').match(/```html\s*([\s\S]*?)\s*```/i);
  if (!htmlBlockMatch?.[1]) return '';

  const prototypeHtml = htmlBlockMatch[1].trim();
  const hasAlpine = /alpinejs|x-data|x-show|x-for|@click|x-on:/i.test(prototypeHtml);
  const hasAlpineCdn = /cdn\.jsdelivr\.net\/npm\/alpinejs|unpkg\.com\/alpinejs/i.test(prototypeHtml);
  const alpineScript = hasAlpine && !hasAlpineCdn
    ? '\n        <script defer src="https://cdn.jsdelivr.net/npm/alpinejs@3.x.x/dist/cdn.min.js"></script>'
    : '';

  return `<!DOCTYPE html>\n<html>\n<head>\n  <meta charset="UTF-8">\n  <meta name="viewport" content="width=device-width, initial-scale=1.0">\n  <style>\n    * { box-sizing: border-box; }\n    body { margin: 0; min-height: 100vh; }\n  </style>${alpineScript}\n</head>\n<body>\n${prototypeHtml}\n</body>\n</html>`;
}

function copyWorkspaceFiles({ workspace, sessionId, projectPath }) {
  const buildFilesList = [];
  if (!workspace || !sessionId) return buildFilesList;

  const wsDir = workspace.workspaceDir(sessionId);
  if (!fs.existsSync(wsDir)) return buildFilesList;

  const copyRecursive = (src, dest) => {
    fs.mkdirSync(dest, { recursive: true });
    const entries = fs.readdirSync(src, { withFileTypes: true });
    for (const entry of entries) {
      const srcPath = path.join(src, entry.name);
      const destPath = path.join(dest, entry.name);
      if (entry.isDirectory()) {
        copyRecursive(srcPath, destPath);
      } else {
        fs.copyFileSync(srcPath, destPath);
        buildFilesList.push(path.relative(projectPath, destPath));
      }
    }
  };

  copyRecursive(wsDir, projectPath);
  return buildFilesList;
}

